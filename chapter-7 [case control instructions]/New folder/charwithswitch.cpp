# include <stdio.h>
int main( )
{
	char c = 'x' ;
	switch ( c ) 
 	{
		case 'v' :
		printf ( "I am in case v \n" ) ;
		break ;
		case 'a' :
		printf ( "I am in case a \n" ) ;
		break ;
		case 'x' :
		printf ( "I am in case x \n" ) ;
		break ;
		default :
		printf ( "I am in default \n" ) ;
	}
return 0 ;
}

